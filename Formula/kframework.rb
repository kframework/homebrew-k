class Kframework < Formula
  desc "K Framework Tools 5.0"
  bottle do
    root_url "https://github.com/kframework/k/releases/download/v5.0.0-496c0b5/"
    rebuild 38
    sha256 "36b05d5557dc7ccbfa2128687374418c255a557527dda549dbd0904071deb4f5" => :mojave
  end

  homepage ""
  url "https://github.com/kframework/k/releases/download/v5.0.0-496c0b5/kframework-5.0.0-src.tar.gz"
  sha256 "30bf8e3a8ca06780c55bc10216caad8d2e0dfd05e8bcd3c563855171443ef2b5"
  depends_on "maven" => :build
  depends_on "cmake" => :build
  depends_on "boost" => :build
  depends_on "haskell-stack" => :build
  depends_on "libyaml"
  depends_on "jemalloc"
  depends_on "llvm@8"
  depends_on "gmp"
  depends_on "mpfr"
  depends_on "z3"
  depends_on "pkg-config" => :build
  depends_on "bison"
  depends_on "flex"

  def install
    ENV["SDKROOT"] = MacOS.sdk_path
    ENV["DESTDIR"] = ""
    ENV["PREFIX"] = "#{prefix}"

    system "mvn", "package", "-DskipTests", "-Dproject.build.type=FastBuild"
    system "package/package"
  end

  test do
    system "true"
  end
end
